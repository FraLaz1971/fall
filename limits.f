      PROGRAM LIMITS
        IMPLICIT NONE
        REAL RMIN,RMAX
        DOUBLE PRECISION DMIN, D1, D2, D3, DMAX
        PARAMETER(RMIN=1.19209E-07,DMIN=2.22045E-16)
        PARAMETER(RMAX=340282346638528859811704183484516925440.0,
     &DMAX=1.797693134862315D308)
        PRINT '(A,F14.12)', 'RMIN=',RMIN
        PRINT '(A,F22.21)', 'DMIN=',DMIN
        D1=DMIN
        D2=1E-21
        D3=D1+D2
        PRINT '(A,F22.21)', 'D3=',D3
        PRINT '(A,F42.1)', 'RMAX= ',RMAX
        PRINT '(A,F312.1)', 'DMAX= ',DMAX
        STOP
      END
